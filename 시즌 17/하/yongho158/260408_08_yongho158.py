# 재귀함수가 뭔가요? 질문은 맨 처음에 고정 
# n == count면 재귀함수는 ~~라네  대답하기 
# n != count면 잘 들어보게. ~~~ 후 func(n, count + 1)로 count값 늘려서 재귀함수 호출하기 
